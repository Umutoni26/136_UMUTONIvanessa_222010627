//This program is going to check the how many kilograms in a bag using switch case
public class mutoni {
    
        public static void main(String args[]){
            int weight=80;
            switch(weight){// switch statement
                case 5:// it like number of bag that we are going to test the kilogram in it
                System.out.println("bag weight 5 kilogram");// this line display the message or out put according to the case
                break;// this break line allow to stop and test another instruction
                case 10:
                System.out.println(" the bag weight 10 kilogram");
                break;
                case 15:
                System.out.println("the bag weight 15 kilogram");
                case 80:
                System.out.println(" the bag weight 20 kilograms");
                break;
                default:
                System.out.println(" we don't have any bag weight to that kilogram");
            }
        }
            
        }
        
    

